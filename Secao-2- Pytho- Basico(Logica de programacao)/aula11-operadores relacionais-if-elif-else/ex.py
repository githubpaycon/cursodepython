"""
Operadores Relacionais
== - Igual
> - Maior Que
>= - Maior ou Igual Que
< - Menor Que
<= - Menor ou Igual Que
!= - Diferente
"""

print('2 é igual a 2?', 2 == 2)  # Vê se é igual e do mesmo tipo
print('2 é maior que 2?', 2 > 2)
print('2 é maior ou igual a 2?', 2 >= 2)
print('2 é menor que 2?', 2 < 2)
print('2 é menor ou igual a 2?', 2 <= 2)


print('3 é maior que 2?', 3 > 2)
print('3 é maior ou igual a 2?', 3 >= 2)
